type InProgress = Arc<Mutex<HashSet<String>>>;  // food names currently being generated by Ollama

#[derive(Debug, Serialize, Deserialize)]
struct FoodItem {
    #[serde(default)]
    id:         i64,
    name:       String,
    #[serde(default)]
    notes:      String,
    #[serde(default)]
    ingredient: String,
}

#[derive(Debug, Serialize, Deserialize)]
struct Condiment {
    #[serde(default)]
    id:   i64,
    name: String,
}

#[derive(Debug, Deserialize)]
struct FoodInput {
    name:  String,
    #[serde(default)]
    notes: String,
    #[serde(default)]
    ingredient: String,
}

// ─── Ollama API response types ─────────────────────────────────────────────────

#[derive(serde::Deserialize)]
struct OllamaResp { choices: Vec<OllamaChoice> }
#[derive(serde::Deserialize)]
struct OllamaChoice { message: OllamaMsg }
#[derive(serde::Deserialize)]
struct OllamaMsg { content: String }

fn valid_category(cat: &str) -> bool {
    matches!(cat, "breakfast" | "curries" | "snacks" | "desserts")
}

// GET /api/foods/:category
async fn list_foods(Path(cat): Path<String>, State(db): State<Db>) -> impl IntoResponse {
    if !valid_category(&cat) {
        return (StatusCode::BAD_REQUEST, Json(serde_json::json!({"error": "invalid category"}))).into_response();
    }
    let conn = db.lock().unwrap();
    let sql = format!("SELECT id, name, notes, ingredient FROM {} ORDER BY id", cat);
    let mut stmt = conn.prepare(&sql).unwrap();
    let items: Vec<FoodItem> = stmt
        .query_map([], |row| {
            Ok(FoodItem {
                id:         row.get(0)?,
                name:       row.get(1)?,
                notes:      row.get::<_, Option<String>>(2)?.unwrap_or_default(),
                ingredient: row.get::<_, Option<String>>(3)?.unwrap_or_default(),
            })
        })
        .unwrap()
        .filter_map(|r| r.ok())
        .collect();
    Json(items).into_response()
}

// POST /api/foods/:category
async fn add_food(
    Path(cat): Path<String>,
    State(db): State<Db>,
    Json(body): Json<FoodInput>,
) -> impl IntoResponse {
    if !valid_category(&cat) {
        return (StatusCode::BAD_REQUEST, Json(serde_json::json!({"error": "invalid category"}))).into_response();
    }
    if body.name.is_empty() || body.name.len() > 18 {
        return (StatusCode::BAD_REQUEST, Json(serde_json::json!({"error": "name must be 1-18 chars"}))).into_response();
    }
    let conn = db.lock().unwrap();
    let sql = format!("INSERT INTO {} (name, notes, ingredient) VALUES (?1,?2,?3)", cat);
    conn.execute(&sql, params![body.name, body.notes, body.ingredient]).unwrap();
    let id = conn.last_insert_rowid();
    Json(serde_json::json!({"id": id})).into_response()
}

// PUT /api/foods/:category/:id
async fn update_food(
    Path((cat, id)): Path<(String, i64)>,
    State(db): State<Db>,
    Json(body): Json<FoodInput>,
) -> impl IntoResponse {
    if !valid_category(&cat) {
        return (StatusCode::BAD_REQUEST, Json(serde_json::json!({"error": "invalid category"}))).into_response();
    }
    if body.name.is_empty() || body.name.len() > 18 {
        return (StatusCode::BAD_REQUEST, Json(serde_json::json!({"error": "name must be 1-18 chars"}))).into_response();
    }
    let conn = db.lock().unwrap();
    let sql = format!("UPDATE {} SET name=?1, notes=?2, ingredient=?3 WHERE id=?4", cat);
    conn.execute(&sql, params![body.name, body.notes, body.ingredient, id]).unwrap();
    Json(serde_json::json!({"ok": true})).into_response()
}

