// POST /api/ingredients
async fn add_ingredient(State(db): State<Db>, Json(body): Json<serde_json::Value>) -> impl IntoResponse {
    let name = body["name"].as_str().unwrap_or("").trim().to_string();
    if name.is_empty() {
        return (StatusCode::BAD_REQUEST, Json(serde_json::json!({"error": "name required"}))).into_response();
    }
    let conn = db.lock().unwrap();
    match conn.execute("INSERT INTO ingredients (name) VALUES (?1)", params![name]) {
        Ok(_) => Json(serde_json::json!({"id": conn.last_insert_rowid()})).into_response(),
        Err(_) => (StatusCode::CONFLICT, Json(serde_json::json!({"error": "already exists"}))).into_response(),
    }
}

