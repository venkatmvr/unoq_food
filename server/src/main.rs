// food-manager — PicoMate backend server (Axum + SQLite + Ollama)
//
// Architecture:
//   Pico W / Adafruit Memento  ──HTTP GET──▶  food-manager (this server)
//                                                  │
//                                         SQLite: food.db
//                                                  │
//                                         Ollama :11434 (nemotron-3-nano)
//
// Packed endpoints (text/plain, device-friendly):
//   GET /api/menu/packed    — all foods by category; device picks one per category by date seed
//   GET /api/browse/packed  — ingredient index + per-ingredient food lists for browse UI
//   GET /api/recipe/prefetch?food=<name>  — kicks off background LLM generation, returns fast
//   GET /api/recipe/packed?food=<name>    — returns recipe or "GENERATING" if still in-flight
//
// Recipe caching strategy:
//   Recipes are stored permanently keyed by food name (UNIQUE(food)).
//   Once generated for any food, subsequent requests return from SQLite instantly — no LLM call.
//   On startup, all uncached foods are pre-generated sequentially in a background task so the
//   device never waits for Ollama during normal use.
//
// JSON endpoints (web UI):
//   CRUD for /api/foods/:category, /api/condiments, /api/ingredients
//   POST /api/recipe/generate?food=<name>  — force-regenerate a cached recipe
//
// Running:
//   ./flash.sh server-build   — release build + launchd restart
//   ./flash.sh logs           — tail server log

